/**
 * Book My Stay App
 *
 * This class represents the entry point of the Hotel Booking Management System.
 * It demonstrates how a Java application starts execution using the main() method
 * and prints a welcome message to the console.
 *
 * The program displays the application name and version information
 * to confirm successful startup.
 *
 * @author Developer
 * @version 1.0
 */
public class BookMyStayApp {

    /**
     * Main method – Entry point of the Java application.
     * The JVM starts program execution from this method.
     *
     * @param args Command line arguments passed during program execution
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("=================================");
        System.out.println("   Welcome to Book My Stay App");
        System.out.println("   Hotel Booking System v1.0");
        System.out.println("=================================");

        // Additional startup information
        System.out.println("Application started successfully.");
        System.out.println("Initializing Hotel Booking Management System...");

        // End of program
        System.out.println("System ready. Thank you for using Book My Stay.");
    }
}